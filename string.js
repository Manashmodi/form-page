function nameToUpper() {
    var fname = document.getElementById('firstname').value;
    fname = fname.charAt(0).toUpperCase() + fname.slice(1).toLowerCase();
    document.getElementById('firstname').value = fname;
}    

function getExp() {
    var exp = document.getElementById('exp').value;
    document.getElementsByTagName('span')[0].innerText = exp;
}  
function checkPassword() {
    var pwd = document.getElementById('password').value;
    var hasLengthRequirement = pwd.length >= 8 && pwd.length <= 15;
    var hasCharacterRequirement = /[a-z]/.test(pwd) && /[A-Z]/.test(pwd) && /[0-9]/.test(pwd) && /[^a-zA-Z0-9]/.test(pwd);
    if (hasLengthRequirement && hasCharacterRequirement) {
      document.getElementById('ppout').style.backgroundColor = "green";
      document.getElementById('ppout').innerText = "RIGHT PATTEN";
    } else if (hasLengthRequirement) {
      document.getElementById('ppout').style.backgroundColor = "orange";
      document.getElementById('ppout').innerText = "Password should include uppercase and lowercase letters, numbers, and special characters";
    } else {
      document.getElementById('ppout').style.backgroundColor = "red";
      document.getElementById('ppout').innerText = "Min length of password is 8";
    }
  }
  


























































































































































































































































































































































































  

